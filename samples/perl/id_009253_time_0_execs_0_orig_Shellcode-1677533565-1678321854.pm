sub hex_to_ascii ($)
{
(my $str = shift) =~ s/([a-fA-F0-9]{2})/chr(hex $1)/eg;
return $str;
}
sub ascii_to_hex ($)
{
(my $str = shift) =~ s/(.|\n)/sprintf("%02lx", ord $1)/eg;
return $str;
}
sub ascii_to_hex2 ($)
{
(my $str = shift) =~ s/(.|\n)/sprintf("%02lx-", ord $1)/eg;
return $str;
}
